export default {
  name: "course_detail",
  data() {},
  mounted() {
    let navbar = document.getElementById("navbar");
    window.onscroll = function () {
      let scrollTop =
        document.body.scrollTop || document.documentElement.scrollTop;

      if (scrollTop > 350 && scrollTop <= 400) {
        // Biến mất ngay
        navbar.style.transition = "opacity 0s"; // Tắt chuyển động trong 0 giây
        navbar.style.opacity = "0";
        navbar.style.position = "absolute";
      } else if (scrollTop > 400) {
        // Xuất hiện với độ trễ 1s
        navbar.style.transition = "opacity 1s"; // Bật chuyển động trong 1 giây
        navbar.style.position = "fixed";
        setTimeout(function () {
          navbar.style.opacity = "1";
        }, 200); // Đặt độ trễ là 1 giây
      } else {
        // Nếu không nằm trong khoảng 350-400, hiển thị bình thường
        navbar.style.transition = "opacity 0.5s"; // Thời gian chuyển động mặc định
        navbar.style.opacity = "1";
        navbar.style.position = "absolute";
      }
    };
    // dropdown lesson khoá học
    let dropdown = document.getElementsByClassName("dropdown-btn");

    for (let i = 0; i < dropdown.length; i++) {
      dropdown[i].addEventListener("click", function () {
        this.classList.toggle("active");
        let dropdownContent = this.nextElementSibling;
        let iconDropdown = document.getElementsByClassName("bi-caret-down"); 

        if (dropdownContent.style.display === "block") {
          dropdownContent.style.display = "none";
        } else {
          dropdownContent.style.display = "block";
        }
      });
    }
  },
};
</script>

